package copilot.exercises.ex02.p01;

public class Main {
    // 10から1までカウントダウンする
    // - Mainクラスを作成してください
    // - mainメソッドの中で以下の処理を実装してください
    //   - forループを使用して、10から1までの数字を逆順で出力してください
    
    public static void main(String[] args) {
        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }
    }
}
